    private void armarTabla() {
        modelo.addColumn("CATEGORIA");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("PRECIO");
        TablaProducto.setModel(modelo);
    }
    
    private void ocultarBoton() {
        LabelNombre.setVisible(false);
        LabelPrecio.setVisible(false);
        JTNombre.setVisible(false);
        JTPrecio.setVisible(false);
        JBAgregar.setVisible(false);
        JBotonAgregarProducto.setVisible(true);
        JTNombre.setText("");
        JTPrecio.setText("");
        jComboBoxCategoria.setVisible(true);
    }
    
    private void mostrarBoton() {
        LabelNombre.setVisible(true);
        LabelPrecio.setVisible(true);
        JTNombre.setVisible(true);
        JTPrecio.setVisible(true);
        JBAgregar.setVisible(true);
        JBotonAgregarProducto.setVisible(false);
        jComboBoxCategoria.setVisible(false);
    }
    
    private void cargarDatos(Producto index) {
        modelo.addRow(new Object[]{index.getCategoria(), index.getNombre(), index.getPrecio()});
        
    }
