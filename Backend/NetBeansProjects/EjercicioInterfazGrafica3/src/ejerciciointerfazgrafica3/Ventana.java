    private void JBotonAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBotonAgregarProductoActionPerformed
        
        if (jComboBoxCategoria.getSelectedIndex() != 0) {
            mostrarBoton();
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione una categoria");
        }

    }//GEN-LAST:event_JBotonAgregarProductoActionPerformed

    private void JBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBAgregarActionPerformed
        
        try {
            String nombre = JTNombre.getText();
            double precio = Double.parseDouble(JTPrecio.getText());
            String categoria = (String) jComboBoxCategoria.getSelectedItem();
            
            if (nombre.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ingrese el nombre del producto");
//            } else if (jComboBoxCategoria.getSelectedIndex() == 0) {
//                JOptionPane.showMessageDialog(this, "Seleccione una categoria gil!!");
            } else {
                Producto aux = new Producto(categoria, nombre, precio);
                cargarDatos(aux);
                ocultarBoton();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese numero en el campo precio");
        }

    }//GEN-LAST:event_JBAgregarActionPerformed

