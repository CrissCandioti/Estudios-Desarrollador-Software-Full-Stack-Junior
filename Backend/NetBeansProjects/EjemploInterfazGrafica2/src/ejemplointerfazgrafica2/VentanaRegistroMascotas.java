    private void BotonCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCargarActionPerformed
        try {
            int edad = Integer.parseInt(TextoEdad.getText());
            String nombre = TextoNombre.getText();
            String raza = TextoRaza.getText();
            if (TextoNombre.getText().isEmpty() || TextoEdad.getText().isEmpty() || TextoRaza.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "No se puede cargar con campos vacios");
            } else {

                Mascotas m = new Mascotas(edad, nombre, raza);
                cargarDatos(m);
                vaciarCasillas();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "La edad es un campo obligatorio ");
        }
    }//GEN-LAST:event_BotonCargarActionPerformed

    private void BotonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEliminarActionPerformed

        try {
            int filas = TablaRegistroMascotas.getSelectedRow();
            if (filas != -1) {
                modelo.removeRow(filas);
            } else if (filas == -1) {
                JOptionPane.showMessageDialog(this, "No se selecciono ningun campo para su eliminacion");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Se a producido un error en su eliminacion");
        }
    }//GEN-LAST:event_BotonEliminarActionPerformed

