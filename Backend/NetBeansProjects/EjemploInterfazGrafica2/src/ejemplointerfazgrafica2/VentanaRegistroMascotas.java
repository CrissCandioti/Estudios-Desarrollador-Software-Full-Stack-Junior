private void cargarDatos(Mascotas m) {
        modelo.addRow(new Object[]{m.getEdad(), m.getNombre(), m.getRaza()});

    }

    private void armarCabecera() {
        modelo.addColumn("NOMBRE");
        modelo.addColumn("RAZA");
        modelo.addColumn("EDAD");
        TablaRegistroMascotas.setModel(modelo);
    }

    private void vaciarCasillas() {
        TextoEdad.setText("");
        TextoNombre.setText("");
        TextoRaza.setText("");
    }
