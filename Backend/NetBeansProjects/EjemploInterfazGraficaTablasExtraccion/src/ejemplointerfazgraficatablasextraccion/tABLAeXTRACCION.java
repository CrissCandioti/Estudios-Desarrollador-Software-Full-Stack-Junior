    private void TablaAlumnoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaAlumnoMouseClicked
        int filaSeleccionada = TablaAlumno.getSelectedRow();
        if (filaSeleccionada != -1) {
            int matricula = (Integer) TablaAlumno.getValueAt(filaSeleccionada, 0);
            String nombre = (String) TablaAlumno.getValueAt(filaSeleccionada, 1);
            String apellido = (String) TablaAlumno.getValueAt(filaSeleccionada, 2);
            TextoMatricula.setText(matricula + "");
            TextoNombre.setText(nombre);
            TextoApellido.setText(apellido);
        }
    }//GEN-LAST:event_TablaAlumnoMouseClicked

