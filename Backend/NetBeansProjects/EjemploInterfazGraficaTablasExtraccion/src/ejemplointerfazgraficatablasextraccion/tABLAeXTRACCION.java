    private void cargar() {
        ArrayList<Alumnos> ListaAlumnos = new ArrayList<>();
        ListaAlumnos.add(new Alumnos(3312, "Cristian", "Candioti"));
        ListaAlumnos.add(new Alumnos(3313, "Diego", "Candioti"));
        ListaAlumnos.add(new Alumnos(3314, "Yamila", "Candioti"));

        for (Alumnos aux : ListaAlumnos) {
            modelo.addRow(new Object[]{aux.getMatricula(), aux.getNombre(), aux.getApellido()});
        }
    }

